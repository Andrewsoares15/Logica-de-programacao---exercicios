#Faça um programa para ver se o número digitado pelo úsuario é múltiplo de 3!
num = int(input("Digite um número: "))
resultado = num % 3
if resultado == 0:
    print("{} é multiplo de 3".format(num))
else :
    print("Não é multiplo de 3")
    
    
    
    
    
    
    
    
    
    
    
